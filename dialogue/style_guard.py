# dialogue/style_guard.py
BAN_CLICHES = (
    "дожд", "кофе", "смотрю в окно", "уют", "капл"
)

def style_constraints(weather_allowed: bool) -> str:
    guard = [
        "- Запрещены клише «дождь/кофе/смотрю в окно/уют/капли по стеклу» без явного запроса пользователя.",
        "- Вопросы должны быть конкретными и привязанными к словам пользователя (что именно/какой/когда в последний раз/зачем).",
        "- Даже в short — 1–2 информативные детали, никаких «угу/класс».",
    ]
    if not weather_allowed:
        guard.append("- Не упоминай погоду в ответе.")
    return "\n".join(guard)
